package com.ryd.model;


public class ClienteForm {

	private int clienteId;
	private String razonSocial;
	private String cuit;
	private String direccion;
	private String telLaboral;
	private String celular;
	private String telParticular;
	private String nextel;
	private String mail;
	private String contacto;
	private String observaciones;
	private double montoSaldo;
	private int saldoId;
	
	public int getClienteId() {
		return clienteId;
	}
	public void setClienteId(int clienteId) {
		this.clienteId = clienteId;
	}
	public String getRazonSocial() {
		return razonSocial;
	}
	public void setRazonSocial(String razonSocial) {
		this.razonSocial = razonSocial;
	}
	public String getCuit() {
		return cuit;
	}
	public void setCuit(String cuit) {
		this.cuit = cuit;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getTelLaboral() {
		return telLaboral;
	}
	public void setTelLaboral(String telLaboral) {
		this.telLaboral = telLaboral;
	}
	public String getCelular() {
		return celular;
	}
	public void setCelular(String celular) {
		this.celular = celular;
	}
	public String getTelParticular() {
		return telParticular;
	}
	public void setTelParticular(String telParticular) {
		this.telParticular = telParticular;
	}
	public String getNextel() {
		return nextel;
	}
	public void setNextel(String nextel) {
		this.nextel = nextel;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getContacto() {
		return contacto;
	}
	public void setContacto(String contacto) {
		this.contacto = contacto;
	}
	public String getObservaciones() {
		return observaciones;
	}
	public void setObservaciones(String observaciones) {
		this.observaciones = observaciones;
	}
	public double getMontoSaldo() {
		return montoSaldo;
	}
	public void setMontoSaldo(double montoSaldo) {
		this.montoSaldo = montoSaldo;
	}
	public int getSaldoId() {
		return saldoId;
	}
	public void setSaldoId(int saldoId) {
		this.saldoId = saldoId;
	}
	
}
